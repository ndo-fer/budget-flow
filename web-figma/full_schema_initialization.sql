-- ── 9. FUNCTIONS & TRIGGERS ─────────────────────────────────────────────

-- Trigger function: Update updated_at column automatically
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Apply updated_at trigger to tables that need it
CREATE TRIGGER trg_budget_categories_updated_at BEFORE UPDATE ON budget_categories FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
CREATE TRIGGER trg_income_sources_updated_at BEFORE UPDATE ON income_sources FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
CREATE TRIGGER trg_recurring_expenses_updated_at BEFORE UPDATE ON recurring_expenses FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
CREATE TRIGGER trg_monthly_plans_updated_at BEFORE UPDATE ON monthly_plans FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();
CREATE TRIGGER trg_wallets_updated_at BEFORE UPDATE ON wallets FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- Function: Recalculate wallet estimated_balance based on transactions
CREATE OR REPLACE FUNCTION recalculate_wallet_estimated_balance()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE wallets
  SET
    estimated_balance = confirmed_balance + COALESCE((
      SELECT
        SUM(CASE WHEN direction = 'in' THEN amount ELSE -amount END)
      FROM wallet_transactions
      WHERE wallet_id = COALESCE(NEW.wallet_id, OLD.wallet_id)
        AND user_id = COALESCE(NEW.user_id, OLD.user_id)
        AND is_duplicate = false
        AND (last_confirmed_at IS NULL OR occurred_at >= last_confirmed_at)
    ), 0),
    updated_at = now()
  WHERE id = COALESCE(NEW.wallet_id, OLD.wallet_id);
  RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql;

-- Apply recalc trigger to wallet_transactions
CREATE TRIGGER trg_recalc_balance_after_tx
AFTER INSERT OR UPDATE OR DELETE ON wallet_transactions
FOR EACH ROW EXECUTE FUNCTION recalculate_wallet_estimated_balance();

-- Function: delete current user (for account self-deletion)
CREATE OR REPLACE FUNCTION delete_user()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  DELETE FROM auth.users WHERE id = auth.uid();
END;
$$;

-- Function: adjust wallet balance atomically
CREATE OR REPLACE FUNCTION adjust_wallet_balance(
  p_wallet_id UUID,
  p_new_balance NUMERIC,
  p_reason TEXT,
  p_source TEXT
)
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_old_balance NUMERIC;
BEGIN
  -- Fetch the current balance of the wallet
  SELECT estimated_balance INTO v_old_balance FROM wallets WHERE id = p_wallet_id AND user_id = auth.uid() FOR UPDATE;
  
  IF v_old_balance IS NULL THEN
    RAISE EXCEPTION 'Wallet not found or access denied';
  END IF;

  -- Log the balance adjustment
  INSERT INTO balance_adjustments (
    user_id,
    wallet_id,
    previous_estimated_balance,
    new_confirmed_balance,
    difference,
    reason,
    source
  ) VALUES (
    auth.uid(),
    p_wallet_id,
    v_old_balance,
    p_new_balance,
    p_new_balance - v_old_balance,
    p_reason,
    p_source
  );

  -- Update confirmed_balance and estimated_balance on the wallet
  UPDATE wallets
  SET 
    confirmed_balance = p_new_balance,
    estimated_balance = p_new_balance,
    last_confirmed_at = now(),
    confidence = 1.0
  WHERE id = p_wallet_id AND user_id = auth.uid();
END;
$$;
